    /// <summary>Look up the line-break class of a Unicode codepoint.</summary>
    public static LineBreakClass Lookup(int codepoint)
    {
        var lo = 0;
        var hi = _ranges.Length - 1;
        while (lo <= hi)
        {
            var mid = (lo + hi) >>> 1;
            ref readonly var r = ref _ranges[mid];
            if (codepoint < r.Start)
            {
                hi = mid - 1;
            }
            else if (codepoint > r.End)
            {
                lo = mid + 1;
            }
            else
            {
                return (LineBreakClass)r.Class;
            }
        }
        return LineBreakClass.XX;
    }

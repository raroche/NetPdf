    /// <summary>Look up the grapheme-cluster-break property of <paramref name="codepoint"/>; returns Other for unlisted codepoints.</summary>
    public static GraphemeClusterBreakProperty Lookup(int codepoint)
    {
        var lo = 0;
        var hi = _ranges.Length - 1;
        while (lo <= hi)
        {
            var mid = (lo + hi) >>> 1;
            ref readonly var r = ref _ranges[mid];
            if (codepoint < r.Start) hi = mid - 1;
            else if (codepoint > r.End) lo = mid + 1;
            else return (GraphemeClusterBreakProperty)r.Property;
        }
        return GraphemeClusterBreakProperty.Other;
    }
